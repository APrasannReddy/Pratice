class ForAlpha3
{
   public static void main(String args[])
   {
	for(int i=1;i<6;i++)
	{
	  char ch ='E'; 
	    for(int j=1;j<6;j++)
	      {
		System.out.print( ch+ " ");
		ch--;
	      }
		
	      
	   System.out.println();
	}
    }
}

